    public void openWindow(JInternalFrame tela){
        Boolean exist = false;
        for(JInternalFrame internal: jDesktopAdmin.getAllFrames()){
            if(internal.getClass().toString().equalsIgnoreCase(tela.getClass().toString())){
                internal.moveToFront();
                exist = true;
            }
        }
        if(!exist){
            jDesktopAdmin.add(tela);
            tela.setVisible(true);
            tela.setClosable(true);
            tela.setIconifiable(true);
        }
    }
