    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTableCliente.getModel();
        modelo.setNumRows(0);
        ClienteDAO cdao = new ClienteDAO();

        for (Cliente c : cdao.read()) {
            modelo.addRow(new Object[]{
                c.getCod_cliente(),
                c.getNome(),
                c.getCpf(),
                c.getSexo_cliente(),
                c.getTelefone(),
                c.getDataNascimento(),
                c.getEndereco_cod_endereco()
            });
        }
    }

    public Cliente setCliente(Cliente cliente) {
        cliente.setNome(txtNome.getText());
        cliente.setCpf(txtCpf.getText());
        cliente.setTelefone(txtTelefone.getText());
        cliente.setSexo_cliente(cbSexo.getItemAt(cbSexo.getSelectedIndex()));
        String dd = txtDataNascimento.getText().substring(0, 2);
        String mm = txtDataNascimento.getText().substring(3, 5);
        String aa = txtDataNascimento.getText().substring(6);
        String data = aa + "-" + mm + "-" + dd;
        cliente.setDataNascimento(data);
        return cliente;
    }

    public Endereco setEndereco(Endereco endereco) {
        endereco.setRua_endereco(txtRua.getText());
        endereco.setNumero_endereco(txtNumero.getText());
        endereco.setComplemento_endereco(txtComplemento.getText());
        endereco.setCep_endereco(txtCep.getText());
        endereco.setCidade_endereco(txtCidade.getText());
        endereco.setUf_endereco(txtUf.getText());
        endereco.setBairro_endereco(txtBairro.getText());
        return endereco;
    }

