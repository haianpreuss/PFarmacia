    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (jTableCliente.getSelectedRow() != -1) {
            Cliente cliente = new Cliente();
            ClienteDAO cDAO = new ClienteDAO();
            Endereco endereco = new Endereco();
            EnderecoDAO eDAO = new EnderecoDAO();

            cliente = setCliente(cliente);
            cliente.setCod_cliente((int) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 0));
            cDAO.update(cliente);

            endereco = setEndereco(endereco);
            endereco.setCod_endereco((int) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 6));
            eDAO.update(endereco);
            readJTable();
        }
        cleanCliente();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void jTableClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClienteMouseClicked
        Endereco endereco = new Endereco();
        EnderecoDAO edao = new EnderecoDAO();

        if (jTableCliente.getSelectedRow() != -1) {
            txtNome.setText(jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 1).toString());
            txtCpf.setText(jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 2).toString());
            cbSexo.setSelectedItem(jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 3).toString());
            txtTelefone.setText(jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 4).toString());
            String aa = jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 5).toString().substring(0, 4);
            String mm = jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 5).toString().substring(5, 7);
            String dd = jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 5).toString().substring(8);
            String data = dd + "/" + mm + "/" + aa;
            txtDataNascimento.setText(data);

            endereco.setCod_endereco((int) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 6));
            endereco = edao.readWhere(endereco);

            txtRua.setText(endereco.getRua_endereco());
            txtNumero.setText(endereco.getNumero_endereco());
            txtComplemento.setText(endereco.getComplemento_endereco());
            txtCep.setText(endereco.getCep_endereco());
            txtCidade.setText(endereco.getCidade_endereco());
            txtUf.setText(endereco.getUf_endereco());
            txtBairro.setText(endereco.getBairro_endereco());
        }
    }//GEN-LAST:event_jTableClienteMouseClicked

