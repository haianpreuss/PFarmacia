    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        FuncionarioDAO fdao = new FuncionarioDAO();
        for(Funcionario f: fdao.read()){
            if(f.getCpf().equals(txtLogin.getText()) && f.getSenha().equals(txtSenha.getText())){
                cod_func = f.getCod_funcionario();
                nome_func = f.getNome();
                nivel = f.getNivel();
                System.out.println(f.getCpf());
                System.out.println(f.getSenha());
            }
        }
        
        if(nivel == 0){
            JMainAdmin jma = new JMainAdmin();
            jma.setVisible(true);
            jma.setTitle("Logado como Administrador: " + cod_func + " - " + nome_func);
            JOptionPane.showMessageDialog(null, "Bem vindo " + nome_func);
            jma.setCod_funcionario(cod_func);
            dispose();
        }else{
            JMainFunc jmf = new JMainFunc();
            jmf.setVisible(true);
            jmf.setTitle("Logado como Funcion√°rio: " + cod_func + " - " + nome_func);
            JOptionPane.showMessageDialog(null, "Bem vindo " + nome_func);
            jmf.setCod_funcionario(cod_func);
            dispose();
        }
        
    }//GEN-LAST:event_btnEntrarActionPerformed

