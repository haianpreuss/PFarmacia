    private void jTableFuncionarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFuncionarioMouseClicked
        Funcionario funcionario = new Funcionario();
        FuncionarioDAO fdao = new FuncionarioDAO();
        Endereco endereco = new Endereco();
        EnderecoDAO edao = new EnderecoDAO();
        
        
        if(jTableFuncionario.getSelectedRow() != -1){
            funcionario.setCod_funcionario((int) jTableFuncionario.getValueAt(jTableFuncionario.getSelectedRow(), 0));
            funcionario = fdao.readWhere(funcionario);
            
            txtNome.setText(funcionario.getNome());
            txtCpf.setText(funcionario.getCpf());
            txtSenha.setText(funcionario.getSenha());
            txtSalario.setText(funcionario.getSalario().toString());
            txtTelefone.setText(funcionario.getTelefone());
            txtDataNascimento.setText(funcionario.getDataNascimento());
            
            endereco.setCod_endereco((int) jTableFuncionario.getValueAt(jTableFuncionario.getSelectedRow(), 4));
            endereco = edao.readWhere(endereco);
            
            txtRua.setText(endereco.getRua_endereco());
            txtNumero.setText(endereco.getNumero_endereco());
            txtComplemento.setText(endereco.getComplemento_endereco());
            txtCep.setText(endereco.getCep_endereco());
            txtCidade.setText(endereco.getCidade_endereco());
            txtUf.setText(endereco.getUf_endereco());
            txtBairro.setText(endereco.getBairro_endereco());
        }
    }//GEN-LAST:event_jTableFuncionarioMouseClicked

    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        if(jTableFuncionario.getSelectedRow() == -1) {
            Funcionario funcionario = new Funcionario();
            FuncionarioDAO fdao = new FuncionarioDAO();
            Endereco endereco = new Endereco();
            EnderecoDAO edao = new EnderecoDAO();
            Integer chave = 0;
            
            endereco = setEndereco(endereco);
            chave = edao.create(endereco);
            
            funcionario = setFuncionario(funcionario);
            funcionario.setEndereco_cod_endereco(chave);
            fdao.create(funcionario);
            
            readJTable();
            
        }
        cleanFuncionario();
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if(jTableFuncionario.getSelectedRow() != -1){
            Funcionario funcionario = new Funcionario();
            FuncionarioDAO fdao = new FuncionarioDAO();
            Endereco endereco = new Endereco();
            EnderecoDAO edao = new EnderecoDAO();
            
            funcionario = setFuncionario(funcionario);
            funcionario.setCod_funcionario((int) jTableFuncionario.getValueAt(jTableFuncionario.getSelectedRow(), 0));
            fdao.update(funcionario);
            
            endereco = setEndereco(endereco);
            endereco.setCod_endereco((int) jTableFuncionario.getValueAt(jTableFuncionario.getSelectedRow(), 4));
            edao.update(endereco);
            readJTable();
        }
        cleanFuncionario();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(jTableFuncionario.getSelectedRow() != -1){
            Funcionario funcionario = new Funcionario();
            FuncionarioDAO fdao = new FuncionarioDAO();
            Endereco endereco = new Endereco();
            EnderecoDAO edao = new EnderecoDAO();
            
            funcionario.setCod_funcionario((int) jTableFuncionario.getValueAt(jTableFuncionario.getSelectedRow(), 0));
            endereco.setCod_endereco((int)jTableFuncionario.getValueAt(jTableFuncionario.getSelectedRow(), 4));
            
            fdao.delete(funcionario);
            edao.delete(endereco);
            readJTable();
            cleanFuncionario();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


