    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTableFuncionario.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO fdao = new FuncionarioDAO();
        
        for(Funcionario f : fdao.read()){
            modelo.addRow(new Object[]{
                f.getCod_funcionario(),
                f.getNome(),
                f.getCpf(),
                f.getTelefone(),
                f.getEndereco_cod_endereco()
            });
        }
    }
    
    public Endereco setEndereco(Endereco endereco) {
        endereco.setRua_endereco(txtRua.getText());
        endereco.setNumero_endereco(txtNumero.getText());
        endereco.setComplemento_endereco(txtComplemento.getText());
        endereco.setCep_endereco(txtCep.getText());
        endereco.setCidade_endereco(txtCidade.getText());
        endereco.setUf_endereco(txtUf.getText());
        endereco.setBairro_endereco(txtBairro.getText());
        return endereco;
    }
    
    public Funcionario setFuncionario(Funcionario funcionario) {
        funcionario.setNome(txtNome.getText());
        funcionario.setCpf(txtCpf.getText());
        funcionario.setTelefone(txtTelefone.getText());
        funcionario.setSalario(Float.parseFloat(txtSalario.getText()));
        funcionario.setSenha(txtSenha.getText());
        String dd = txtDataNascimento.getText().substring(0, 2);
        String mm = txtDataNascimento.getText().substring(3, 5);
        String aa = txtDataNascimento.getText().substring(6);
        String data = aa + "-" + mm + "-" + dd;
        funcionario.setDataNascimento(data);
        return funcionario;
    }
    
