    private void jTableFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFornecedorMouseClicked
        Endereco endereco = new Endereco();
        EnderecoDAO edao = new EnderecoDAO();

        if (jTableFornecedor.getSelectedRow() != -1) {
            txtNome.setText(jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 1).toString());
            txtCnpj.setText(jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 2).toString());
            txtTelefone.setText(jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 3).toString());

            endereco.setCod_endereco((int) jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 4));
            endereco = edao.readWhere(endereco);

            txtRua.setText(endereco.getRua_endereco());
            txtNumero.setText(endereco.getNumero_endereco());
            txtComplemento.setText(endereco.getComplemento_endereco());
            txtCep.setText(endereco.getCep_endereco());
            txtCidade.setText(endereco.getCidade_endereco());
            txtUf.setText(endereco.getUf_endereco());
            txtBairro.setText(endereco.getBairro_endereco());
        }
    }//GEN-LAST:event_jTableFornecedorMouseClicked

    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        if (jTableFornecedor.getSelectedRow() == -1) {
            Fornecedor fornecedor = new Fornecedor();
            FornecedorDAO fdao = new FornecedorDAO();
            Endereco endereco = new Endereco();
            EnderecoDAO edao = new EnderecoDAO();
            Integer chave = 0;

            endereco = setEndereco(endereco);
            chave = edao.create(endereco);

            fornecedor = setFornecedor(fornecedor);
            fornecedor.setEndereco_cod_endereco(chave);
            fdao.create(fornecedor);

            readJTable();
            
        }
        cleanFornecedor();
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if(jTableFornecedor.getSelectedRow() != 1){
            Fornecedor fornecedor = new Fornecedor();
            FornecedorDAO fdao = new FornecedorDAO();
            Endereco endereco = new Endereco();
            EnderecoDAO edao = new EnderecoDAO();
            
            fornecedor = setFornecedor(fornecedor);
            fornecedor.setCod_fornecedor((int) jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 0));
            fornecedor.setEndereco_cod_endereco(((int) jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 4)));
            fdao.update(fornecedor);
            
            endereco = setEndereco(endereco);
            endereco.setCod_endereco((int) jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 4));
            edao.update(endereco);
            readJTable();
            
            cleanFornecedor();
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(jTableFornecedor.getSelectedRow() != -1){
            Fornecedor fornecedor = new Fornecedor();
            FornecedorDAO fdao = new FornecedorDAO();
            Endereco endereco = new Endereco();
            EnderecoDAO edao = new EnderecoDAO();
            
            fornecedor.setCod_fornecedor((int) jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 0));
            endereco.setCod_endereco((int) jTableFornecedor.getValueAt(jTableFornecedor.getSelectedRow(), 4));
            
            fdao.delete(fornecedor);
            edao.delete(endereco);
            readJTable();
            cleanFornecedor();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


