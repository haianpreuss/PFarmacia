    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTableFornecedor.getModel();
        modelo.setNumRows(0);
        FornecedorDAO fdao = new FornecedorDAO();

        for (Fornecedor f : fdao.read()) {
            modelo.addRow(new Object[]{
                f.getCod_fornecedor(),
                f.getNome_fornecedor(),
                f.getCnpj_fornecedor(),
                f.getTelefone_fornecedor(),
                f.getEndereco_cod_endereco()
            });
        }
    }

    public Endereco setEndereco(Endereco endereco) {
        endereco.setRua_endereco(txtRua.getText());
        endereco.setNumero_endereco(txtNumero.getText());
        endereco.setComplemento_endereco(txtComplemento.getText());
        endereco.setCep_endereco(txtCep.getText());
        endereco.setCidade_endereco(txtCidade.getText());
        endereco.setUf_endereco(txtUf.getText());
        endereco.setBairro_endereco(txtBairro.getText());
        return endereco;
    }

    public Fornecedor setFornecedor(Fornecedor fornecedor) {
        fornecedor.setNome_fornecedor(txtNome.getText());
        fornecedor.setCnpj_fornecedor(txtCnpj.getText());
        fornecedor.setTelefone_fornecedor(txtTelefone.getText());
        return fornecedor;
    }

